#include <math.h>
#include "vector.h"

//////////////////////////////////////////////////////////////////
// Implemenations of Vector 2D functions
//////////////////////////////////////////////////////////////////
float vec2_length(vec2_t v)
{
    return sqrt(v.x * v.x + v.y * v.y );
}

//////////////////////////////////////////////////////////////////
// Implemenations of Vector 3D functions
//////////////////////////////////////////////////////////////////
float vec3_length(vec3_t v)
{
    return sqrt(v.x * v.x + v.y * v.y + v.z * v.z);
}